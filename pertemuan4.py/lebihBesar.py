# membuat program input yang meminta input dua 
# bilangan dan menampilkan bilangan yang lebih besar

bilanganA = int(input("Masukan bilangan A : "))
bilanganB = int(input("Masukan bilangan B : "))

if bilanganA > bilanganB :
    hasil = "Bilangan A lebih besar"
else :
    hasil = "Bilangan B lebih besar"

print(hasil)